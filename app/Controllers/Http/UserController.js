  /**
   * Render a form to be used for creating a new user.
   * GET users/create
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async create ({ request, response, view }) {
  }

  /**
   * Create/save a new user.
   * POST users
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   */
  async store ({ request, response, session }) {
    const data = request.only(['name', 'nickname', 'email', 'password'])
    
    try {
      await User.create(data)
      session.flash({ message: 'Cadastro Efetuado com sucesso!', type:'success' })
      return response.redirect('/login')
    } catch (error) {
      console.log(error);
      session.flash({ message: 'Algo de errado aconteceu', type:'danger' })
      return response.redirect('/login')
    }
  }

  /**
   * Display a single user.
   * GET users/:id
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async login ({ request, response, session, auth }) {
    const {email, password} = request.all()
    try {
      await auth.attempt(email, password)
      return response.redirect('/manager-posts')
    } catch (error) {
      console.log(error);
      session.flash({ message: 'Autenticação não pode ser efetuada.', type:'danger' })
      return response.redirect('/login')
    } 
  }

  async logout ({ request, response, session, auth }) {
    try {
      await auth.logout()
      return response.redirect('/login')
    } catch (error) {
      
    }

  }

